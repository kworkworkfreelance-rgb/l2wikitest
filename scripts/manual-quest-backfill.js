const duplicateFacts = (facts = []) => facts.map((fact) => ({ ...fact }));

const createQuestBackfill = ({ summary, facts = [], intro = [], prep = [], steps = [], rewards = [], notes = [] }) => ({
    summary,
    meta: duplicateFacts(facts),
    sidebarFacts: duplicateFacts(facts),
    blocks: [
        intro.length
            ? {
                  type: 'prose',
                  title: 'О квесте',
                  paragraphs: intro,
              }
            : null,
        prep.length
            ? {
                  type: 'list',
                  title: 'Что подготовить',
                  style: 'check',
                  items: prep,
              }
            : null,
        steps.length
            ? {
                  type: 'steps',
                  title: 'Пошаговое прохождение',
                  style: 'ordered',
                  items: steps,
              }
            : null,
        rewards.length
            ? {
                  type: 'list',
                  title: 'Награды и результат',
                  items: rewards,
              }
            : null,
        notes.length
            ? {
                  type: 'callout',
                  title: 'Полезные заметки',
                  tone: 'info',
                  items: notes,
              }
            : null,
    ].filter(Boolean),
});

